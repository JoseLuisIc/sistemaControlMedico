    private void btnAddEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEliminarActionPerformed
        // TODO add your handling code here:
        cc.openConnection();
        int resp = cc.deleteUser(idUsuario);
        cc.closeConnection();
        if(resp == 1){
            System.out.println("El usuario fue elimnado con exito");
            txtMensaje.setText("El usuario fue elimnado con exito");
            actualizarTabla(0);
        }
        else{
            System.out.println("El usuario no fue elimnado con exito");
            txtMensaje.setText("El usuario no fue elimnado con exito");
            actualizarTabla(0);
        }
    }//GEN-LAST:event_btnAddEliminarActionPerformed

