    public vistaUsuarioPrincipal() throws IOException {
        cc.openConnection();
        modeloUsuarios = cc.modeloUsuarios(columna);
        initComponents();
        jTableUsuarios.addMouseListener(new java.awt.event.MouseAdapter() {
            @Override
            public void mouseClicked(java.awt.event.MouseEvent evt) {
                int row = jTableUsuarios.rowAtPoint(evt.getPoint());
                int col = 0;
                if (row >= 0 && col >= 0) {
                    String valor = jTableUsuarios.getModel().getValueAt(row, col).toString(); //Tomo el valor de el modelo de la tabla
                    idUsuario = Integer.parseInt(valor); //Obtengo el valor del textfield
                    System.out.println(idUsuario);
                }
            }
        });
        logger.info("SistemaLogger.log", "Usuario: Actividad: Se inicia el Panel de Usuarios()");
        cc.closeConnection();
    }
    void actualizarTabla(int busqueda){
        cc.openConnection();
        if(busqueda == 0){
            modeloUsuarios = cc.modeloUsuarios(columna);
        }
        else if(busqueda == 1){
            modeloUsuarios = cc.modeloUsuarios(columna,txtBuscarUsuario.getText());  
        }
        cc.closeConnection();
        jTableUsuarios.setModel(modeloUsuarios);
        modeloUsuarios.fireTableDataChanged();
    }
